package com.example.flashcard_app;

public class User {

    String questions;
    int count;

    public User(){}

    public void setQuestions(String q){
        this.questions = q;
    }
    public String getQuestions(){
        return this.questions;

    }
    public void setCount(int count){
        this.count = count;
    }
    public int getCount(){
        return this.count;
    }





}
